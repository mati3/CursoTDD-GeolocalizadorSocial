#!/usr/bin/env python
# coding: utf-8

class Post:
    
    def Post (autor, tags, social, location):
        self.autor = autor
        self.tags = tags
        self.social = social
        self.location = location
        
    def getAutor():
        return self.autor
    
    def getTags():
        return self.tags
    
    def getSocial():
        return self.social
    
    def getLocation():
        return self.location
    

